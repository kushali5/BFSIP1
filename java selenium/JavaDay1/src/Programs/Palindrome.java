package Programs;

public class Palindrome {

	public static void main(String[] args) {
		int n=45354,temp,sum=0,rem;
		
		temp=n;
		while(n>0)
		{
			rem=n%10;
			sum=sum*10+rem;
			n=n/10;
			
		}
		if(temp==sum)
			System.out.println("Palindrome number");
		else
			System.out.println("not Palindrom");

	}

}
